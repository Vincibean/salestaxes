/* Join all Poiuyts with the base Category */
INSERT
INTO CATEGORY_POIUYT 
	(
		CATEGORY_ID, 
		POIUYT_ID
	)
SELECT 
	CATEGORY.ID, 
	POIUYT.ID 
FROM 
	CATEGORY 
	CROSS JOIN 
	POIUYT 
	WHERE 
		CATEGORY.NAME='Poiuyt Category' 
	AND 
		POIUYT.NAME='"How I did It"; Author: "Victor Frankenstein"';
		
